#!/usr/bin/env python3
"""
gen_help.py -- Parse config/help.ini and generate ASM tile data.

Output:
  ../assets/help.inc -- .word entries for the SNES terminal help response

Data format (consumed by print_help_response in main.asm):
  $3Cxx / $3Dxx  -- tilemap word for a printable character
  $FFFF          -- newline (advance cursor_y, reset cursor_x)
  $0000          -- end sentinel

Limits (enforced with sys.exit):
  Max 30 chars/line  (USABLE_COLS in main.asm)
  ASCII 0x20..0x7E only

Usage (from snes/ directory):
    make
"""

import sys
from pathlib import Path

MAX_COLS = 30


def char_to_tile_word(ch: str) -> int:
    c = ord(ch) - 0x20
    n = (c // 8) * 32 + (c % 8) * 2
    return 0x3C00 | n


def main() -> None:
    ini_path = Path(__file__).parent.parent.parent / "config" / "help.ini"
    if not ini_path.exists():
        sys.exit(f"Error: help.ini not found at {ini_path}")

    raw_lines = ini_path.read_text(encoding="utf-8").splitlines()

    content_lines = [
        line.rstrip()
        for line in raw_lines
        if not line.lstrip().startswith((";", "#"))
    ]

    # Remove trailing blank lines only (leading blanks are meaningful in help text)
    while content_lines and content_lines[-1] == "":
        content_lines.pop()

    for i, line in enumerate(content_lines, 1):
        if len(line) > MAX_COLS:
            sys.exit(
                f"Error: line {i} has {len(line)} characters "
                f"(max {MAX_COLS}, USABLE_COLS in main.asm):\n  {line!r}"
            )
        for j, ch in enumerate(line, 1):
            if not (0x20 <= ord(ch) <= 0x7E):
                sys.exit(
                    f"Error: line {i}, col {j}: character {ch!r} "
                    f"(U+{ord(ch):04X}) is not ASCII printable (0x20..0x7E)"
                )

    out_dir  = Path(__file__).parent.parent / "assets"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / "help.inc"

    total_chars = sum(len(line) for line in content_lines)

    with open(out_path, "w", encoding="utf-8") as f:
        f.write("; Auto-generated by tools/gen_help.py -- do not edit manually.\n")
        f.write(f"; Source: config/help.ini -- {len(content_lines)} lines, {total_chars} chars\n")

        for line in content_lines:
            if line:
                for ch in line:
                    word = char_to_tile_word(ch)
                    f.write(f"    .word ${word:04X}  ; {ch!r}\n")
            f.write("    .word $FFFF  ; newline\n")

        f.write("    .word $0000  ; sentinel\n")

    print(f"help.inc        : {len(content_lines)} lines, {total_chars} chars")


if __name__ == "__main__":
    main()
